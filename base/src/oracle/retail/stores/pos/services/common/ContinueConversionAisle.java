package oracle.retail.stores.pos.services.common;

import oracle.retail.stores.foundation.tour.application.Letter;
import oracle.retail.stores.foundation.tour.ifc.BusIfc;
import oracle.retail.stores.pos.services.PosLaneActionAdapter;

/**
 * Mails a Continue letter. This class is used to traverse from another letter
 * (such as Yes or No or Ok or another UI-based letter) to a Continue letter.
 * 
 * @version $Revision: /rgbustores_13.4x_generic_branch/1 $
 */
public class ContinueConversionAisle extends PosLaneActionAdapter
{
    private static final long serialVersionUID = -7967503522410962896L;

    /**
     * lane name constant
     */
    public static final String LANENAME = "ContinueConversionAisle";

    /**
     * revision number supplied by source-code-control system
     **/
    public static String revisionNumber = "$Revision: /rgbustores_13.4x_generic_branch/1 $";

    /**
     * Mails a Continue letter.
     * 
     * @param bus the bus traversing this lane
     */
    public void traverse(BusIfc bus)
    {
        Letter letter = new Letter(CommonLetterIfc.CONTINUE);
        bus.mail(letter, BusIfc.CURRENT);
    }

    /**
     * Returns the revision number of the class.
     * 
     * @return String representation of revision number
     */
    public String getRevisionNumber()
    {
        // return string
        return (revisionNumber);
    }
}
